/* ---- Konsole -------------------------------------------------------------
 * Konsole has no single theme file: colors live in a `.colorscheme`
 * (decimal "r,g,b", not hex) under the data dir, and a profile picks
 * one up via its own `ColorScheme=` key. We always (re)write the
 * colorscheme; we only patch a profile if one already exists to patch
 * -- inventing a whole new, valid `.profile` from scratch risks
 * producing something Konsole rejects outright. */

static char *decimal_rgb(const Theme *theme, const char *hex) {
    unsigned int r, g, b;
    sscanf(strip_hash(hex), "%02x%02x%02x", &r, &g, &b);
    (void)theme;
    return xsprintf("%u,%u,%u", r, g, b);
}

static void append_konsole_color_section(StrBuf *sb, const char *section, const char *hex) {
    char *dec = decimal_rgb(NULL, hex);
    char *chunk = xsprintf("[%s]\nColor=%s\n\n", section, dec);
    strbuf_append(sb, chunk);
    free(chunk);
    free(dec);
}

/* Returns the value of `key=` under `[section]` in `ini_text` (a
 * whole already-read file), or NULL if either is missing. Caller must
 * free(). A small, deliberately forgiving INI reader -- good enough
 * for the handful of keys calm-shell reads out of Konsole's own files,
 * not a general-purpose parser. */
static char *ini_get(const char *ini_text, const char *section, const char *key) {
    if (!ini_text) {
        return NULL;
    }
    size_t line_count = 0;
    char **lines = split_lines(ini_text, &line_count);
    char *result = NULL;
    bool in_section = section[0] == '\0'; /* empty section = top of file */
    char *want_header = xsprintf("[%s]", section);
    size_t key_len = strlen(key);
    for (size_t i = 0; i < line_count && !result; i++) {
        char *trimmed = xstrdup(lines[i]);
        trim_in_place(trimmed);
        if (trimmed[0] == '[') {
            in_section = strcmp(trimmed, want_header) == 0;
        } else if (in_section && strncmp(trimmed, key, key_len) == 0 && trimmed[key_len] == '=') {
            result = xstrdup(trimmed + key_len + 1);
        }
        free(trimmed);
    }
    free(want_header);
    free_lines(lines, line_count);
    return result;
}

/* Replaces `key=`'s value under `[section]` in `ini_text` if present,
 * inserts a new `key=value` line right after the section header if
 * the section exists but the key doesn't, or appends a whole new
 * `[section]` block at the end if the section itself is missing.
 * Returns a newly allocated string; never mutates `ini_text`. */
static char *ini_set(const char *ini_text, const char *section, const char *key, const char *value) {
    size_t line_count = 0;
    char **lines = ini_text ? split_lines(ini_text, &line_count) : NULL;
    char *want_header = xsprintf("[%s]", section);
    size_t key_len = strlen(key);

    StrBuf out;
    strbuf_init(&out);
    bool in_section = false;
    bool section_found = false;
    bool key_written = false;
    for (size_t i = 0; i < line_count; i++) {
        char *trimmed = xstrdup(lines[i]);
        trim_in_place(trimmed);
        if (trimmed[0] == '[') {
            if (in_section && !key_written) {
                char *kv = xsprintf("%s=%s\n", key, value);
                strbuf_append(&out, kv);
                free(kv);
                key_written = true;
            }
            in_section = strcmp(trimmed, want_header) == 0;
            if (in_section) {
                section_found = true;
            }
            strbuf_append(&out, lines[i]);
            strbuf_append(&out, "\n");
        } else if (in_section && !key_written && strncmp(trimmed, key, key_len) == 0 && trimmed[key_len] == '=') {
            char *kv = xsprintf("%s=%s\n", key, value);
            strbuf_append(&out, kv);
            free(kv);
            key_written = true;
        } else {
            strbuf_append(&out, lines[i]);
            strbuf_append(&out, "\n");
        }
        free(trimmed);
    }
    if (in_section && !key_written) {
        char *kv = xsprintf("%s=%s\n", key, value);
        strbuf_append(&out, kv);
        free(kv);
        key_written = true;
    }
    if (!section_found) {
        char *block = xsprintf("[%s]\n%s=%s\n", section, key, value);
        strbuf_append(&out, block);
        free(block);
    }

    free(want_header);
    if (lines) {
        free_lines(lines, line_count);
    }
    return strbuf_take(&out);
}

static bool sync_konsole(const Theme *theme, const TermAppearance *ap, char **out_detail) {
    char *data_dir = xdg_data_dir();
    if (!data_dir) {
        if (out_detail) {
            *out_detail = xstrdup("konsole: could not resolve the XDG data directory");
        }
        return false;
    }
    char *konsole_data = join_path(data_dir, "konsole");
    free(data_dir);
    if (!mkdir_recursive(konsole_data)) {
        free(konsole_data);
        if (out_detail) {
            *out_detail = xstrdup("konsole: could not create ~/.local/share/konsole");
        }
        return false;
    }

    TermColorScheme cs;
    color_scheme_build(theme, &cs);

    StrBuf sb;
    strbuf_init(&sb);
    strbuf_append(&sb, "[General]\nDescription=Calm Shell\nOpacity=");
    char *op = xsprintf("%.2f\nWallpaper=\n\n", ap->opacity);
    strbuf_append(&sb, op);
    free(op);

    append_konsole_color_section(&sb, "Background", cs.ansi[0]);
    append_konsole_color_section(&sb, "BackgroundIntense", cs.ansi[8]);
    append_konsole_color_section(&sb, "Foreground", cs.ansi[7]);
    append_konsole_color_section(&sb, "ForegroundIntense", cs.ansi[15]);
    static const char *const slot_names[8] = {"Color0", "Color1", "Color2", "Color3",
                                               "Color4", "Color5", "Color6", "Color7"};
    for (int i = 0; i < 8; i++) {
        append_konsole_color_section(&sb, slot_names[i], cs.ansi[i]);
        char *intense_name = xsprintf("%sIntense", slot_names[i]);
        append_konsole_color_section(&sb, intense_name, cs.ansi[8 + i]);
        free(intense_name);
    }

    char *contents = strbuf_take(&sb);
    char *colorscheme_path = join_path(konsole_data, "CalmShell.colorscheme");
    bool wrote = overwrite_file(colorscheme_path, contents);
    free(contents);

    /* Best-effort: point the default profile at it, if we can find
     * one. Konsole's own default-profile pointer lives in
     * konsolerc's [Desktop Entry] section, in the user's *config*
     * dir (not the data dir the profile/colorscheme files live in). */
    bool profile_patched = false;
    bool profile_found = false;
    char *config_dir_path = xdg_config_dir();
    if (wrote && config_dir_path) {
        char *konsolerc_path = join_path(config_dir_path, "konsolerc");
        char *konsolerc = read_file_to_string(konsolerc_path);
        char *default_profile = ini_get(konsolerc, "Desktop Entry", "DefaultProfile");
        free(konsolerc);
        free(konsolerc_path);
        if (default_profile && default_profile[0] != '\0') {
            char *profile_path = join_path(konsole_data, default_profile);
            char *profile_text = read_file_to_string(profile_path);
            if (profile_text) {
                profile_found = true;
                char *patched = ini_set(profile_text, "Appearance", "ColorScheme", "CalmShell");
                profile_patched = overwrite_file(profile_path, patched);
                free(patched);
            }
            free(profile_text);
            free(profile_path);
        }
        free(default_profile);
    }
    free(config_dir_path);

    if (out_detail) {
        if (!wrote) {
            *out_detail = xstrdup("konsole: failed to write CalmShell.colorscheme");
        } else if (profile_patched) {
            *out_detail = xstrdup("konsole: theme synced (default profile now uses the CalmShell color scheme)");
        } else if (profile_found) {
            *out_detail = xstrdup("konsole: wrote CalmShell.colorscheme, but could not update the default profile");
        } else {
            *out_detail = xstrdup(
                "konsole: wrote CalmShell.colorscheme -- select it under Settings > Edit Current Profile > "
                "Appearance (no default profile found to update automatically)");
        }
    }

    color_scheme_free(&cs);
    free(colorscheme_path);
    free(konsole_data);
    return wrote;
}

/* ---- GNOME Terminal --------------------------------------------------
 * No config file at all -- everything lives in dconf, read and
 * written through `gsettings`. We resolve the default profile's UUID
 * once and then set each key directly on its
 * org.gnome.Terminal.Legacy.Profile relocatable schema instance. */

static char *gsettings_capture(const char *const argv[]) {
    return process_capture_stdout((char *const *)argv);
}

static bool gsettings_run(const char *const argv[]) {
    int status = -1;
    bool spawned = process_run_foreground((char *const *)argv, &status);
    return spawned && status == 0;
}

static bool sync_gnome_terminal(const Theme *theme, const TermAppearance *ap, char **out_detail) {
    if (!process_command_exists("gsettings")) {
        if (out_detail) {
            *out_detail = xstrdup("gnome-terminal: `gsettings` not found, skipping sync");
        }
        return false;
    }

    const char *get_default_argv[] = {"gsettings", "get", "org.gnome.Terminal.ProfilesList", "default", NULL};
    char *raw_uuid = gsettings_capture(get_default_argv);
    if (!raw_uuid) {
        if (out_detail) {
            *out_detail = xstrdup("gnome-terminal: could not read the default profile UUID");
        }
        return false;
    }
    trim_in_place(raw_uuid);
    /* gsettings prints GVariant strings single-quoted: 'uuid-here'. */
    char *uuid = raw_uuid;
    size_t len = strlen(uuid);
    if (len >= 2 && uuid[0] == '\'' && uuid[len - 1] == '\'') {
        uuid[len - 1] = '\0';
        uuid++;
    }

    char *schema = xsprintf("org.gnome.Terminal.Legacy.Profile:/org/gnome/terminal/legacy/profiles:/:%s/", uuid);
    free(raw_uuid);

    TermColorScheme cs;
    color_scheme_build(theme, &cs);

    StrBuf palette;
    strbuf_init(&palette);
    strbuf_append(&palette, "[");
    for (int i = 0; i < 16; i++) {
        char *item = xsprintf("%s'%s'", i == 0 ? "" : ", ", cs.ansi[i]);
        strbuf_append(&palette, item);
        free(item);
    }
    strbuf_append(&palette, "]");
    char *palette_value = strbuf_take(&palette);

    char *bg_value = xsprintf("'%s'", cs.background);
    char *fg_value = xsprintf("'%s'", cs.foreground);
    char *cursor_bg = xsprintf("'%s'", cs.cursor);
    char *cursor_fg = xsprintf("'%s'", cs.background);
    char *font_value = xsprintf("'%s %.0f'", ap->font_family, ap->font_size);
    int transparency_percent = (int)((1.0 - ap->opacity) * 100.0 + 0.5);
    char *transparency_value = xsprintf("%d", transparency_percent);
    const char *cursor_shape = ap->cursor_style == CURSOR_STYLE_BLOCK   ? "block"
                                : ap->cursor_style == CURSOR_STYLE_UNDERLINE ? "underline"
                                                                              : "ibeam";
    const char *blink_mode = ap->cursor_blink ? "on" : "off";
    bool transparent = ap->opacity < 1.0;

    struct {
        const char *key;
        const char *value;
    } settings[] = {
        {"use-theme-colors", "false"},
        {"background-color", bg_value},
        {"foreground-color", fg_value},
        {"palette", palette_value},
        {"bold-color-same-as-fg", "true"},
        {"cursor-colors-set", "true"},
        {"cursor-background-color", cursor_bg},
        {"cursor-foreground-color", cursor_fg},
        {"cursor-shape", cursor_shape},
        {"cursor-blink-mode", blink_mode},
        {"use-system-font", "false"},
        {"font", font_value},
        {"use-transparent-background", transparent ? "true" : "false"},
        {"background-transparency-percent", transparency_value},
    };

    bool all_ok = true;
    for (size_t i = 0; i < sizeof(settings) / sizeof(settings[0]); i++) {
        char *argv_schema = xsprintf("%s", schema); /* process_run_foreground's argv isn't const-correct upstream */
        const char *argv[] = {"gsettings", "set", argv_schema, settings[i].key, settings[i].value, NULL};
        if (!gsettings_run(argv)) {
            all_ok = false;
        }
        free(argv_schema);
    }

    if (out_detail) {
        *out_detail = all_ok
                          ? xstrdup("gnome-terminal: theme synced (default profile updated via gsettings)")
                          : xstrdup("gnome-terminal: synced most settings, but one or more `gsettings set` calls failed");
    }

    color_scheme_free(&cs);
    free(schema);
    free(palette_value);
    free(bg_value);
    free(fg_value);
    free(cursor_bg);
    free(cursor_fg);
    free(font_value);
    free(transparency_value);
    return all_ok;
}

/* ---- dispatch ---------------------------------------------------------- */

bool emulator_sync_write(TermEmulatorKind kind, const Theme *theme, const TermAppearance *appearance,
                          char **out_detail) {
    switch (kind) {
        case TERM_EMU_KITTY:
            return sync_kitty(theme, appearance, out_detail);
        case TERM_EMU_ALACRITTY:
            return sync_alacritty(theme, appearance, out_detail);
        case TERM_EMU_WEZTERM:
            return sync_wezterm(theme, appearance, out_detail);
        case TERM_EMU_FOOT:
            return sync_foot(theme, appearance, out_detail);
        case TERM_EMU_GHOSTTY:
            return sync_ghostty(theme, appearance, out_detail);
        case TERM_EMU_KONSOLE:
            return sync_konsole(theme, appearance, out_detail);
        case TERM_EMU_GNOME_TERMINAL:
            return sync_gnome_terminal(theme, appearance, out_detail);
        case TERM_EMU_UNKNOWN:
        default:
            if (out_detail) {
                *out_detail = xstrdup("no supported terminal emulator detected -- nothing synced");
            }
            return true;
    }
}
