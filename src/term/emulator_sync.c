/* ---- small shared helpers ------------------------------------------- */

/* "#RRGGBB" -> "RRGGBB" (no leading '#'), for the formats (foot,
 * Konsole's decimal form aside) that want the hash omitted. Returns a
 * pointer into `hex` itself, not a new allocation. */
static const char *strip_hash(const char *hex) {
    return (hex[0] == '#') ? hex + 1 : hex;
}

static char *config_subdir(const char *app) {
    char *base = xdg_config_dir();
    if (!base) {
        return NULL;
    }
    char *out = join_path(base, app);
    free(base);
    return out;
}

/* Ensures `main_config_path` references `generated_path` via
 * `directive_line` (a complete, newline-terminated line). If the
 * generated filename already appears anywhere in the file, does
 * nothing (already wired, however the user or a previous sync phrased
 * it). If the file doesn't exist yet, creates it containing just that
 * line. Otherwise appends the line, preceded by a comment explaining
 * where it came from, to the end -- the existing content is never
 * touched. `comment_prefix` is that format's comment marker ("#",
 * "//", "--", ...). */
static bool ensure_include_line(const char *main_config_path, const char *generated_basename,
                                 const char *directive_line, const char *comment_prefix) {
    if (file_contains(main_config_path, generated_basename)) {
        return true;
    }
    if (!path_exists(main_config_path)) {
        return overwrite_file(main_config_path, directive_line);
    }
    char *addition = xsprintf("\n%s Added by calm-shell (`calm sync`) -- keeps this terminal's\n"
                               "%s theme matched to Calm-shell's active theme.\n%s",
                               comment_prefix, comment_prefix, directive_line);
    bool ok = append_to_file(main_config_path, addition);
    free(addition);
    return ok;
}

static const char *cursor_style_kitty(CursorStyle s) {
    switch (s) {
        case CURSOR_STYLE_BLOCK:
            return "block";
        case CURSOR_STYLE_UNDERLINE:
            return "underline";
        default:
            return "beam";
    }
}

static const char *cursor_style_ghostty(CursorStyle s) {
    switch (s) {
        case CURSOR_STYLE_BLOCK:
            return "block";
        case CURSOR_STYLE_UNDERLINE:
            return "underline";
        default:
            return "bar"; /* Ghostty's name for a beam/I-beam cursor. */
    }
}

/* ---- Kitty ------------------------------------------------------------
 * https://sw.kovidgoyal.net/kitty/conf/ -- `include` is a simple
 * relative-or-absolute directive kitty resolves at load time, so
 * wiring the generated file in is a single idempotent append. */

static bool sync_kitty(const Theme *theme, const TermAppearance *ap, char **out_detail) {
    char *dir = config_subdir("kitty");
    if (!dir || !mkdir_recursive(dir)) {
        free(dir);
        if (out_detail) {
            *out_detail = xstrdup("kitty: could not create ~/.config/kitty");
        }
        return false;
    }

    TermColorScheme cs;
    color_scheme_build(theme, &cs);

    StrBuf sb;
    strbuf_init(&sb);
    strbuf_append(&sb, "# Generated by calm-shell (`calm sync`) -- edits here are overwritten\n"
                        "# on the next sync. Change the active Calm-shell theme instead.\n\n");
    char *line = xsprintf("background %s\nforeground %s\ncursor %s\n\n", cs.background, cs.foreground, cs.cursor);
    strbuf_append(&sb, line);
    free(line);

    line = xsprintf("cursor_shape %s\ncursor_blink_interval %s\n\n", cursor_style_kitty(ap->cursor_style),
                     ap->cursor_blink ? "0.5" : "0");
    strbuf_append(&sb, line);
    free(line);

    line = xsprintf("background_opacity %.2f\n", ap->opacity);
    strbuf_append(&sb, line);
    free(line);
    line = xsprintf("window_padding_width %lld\n", ap->padding);
    strbuf_append(&sb, line);
    free(line);
    line = xsprintf("hide_window_decorations %s\n\n", ap->window_decorations ? "no" : "yes");
    strbuf_append(&sb, line);
    free(line);

    line = xsprintf("font_family %s\nfont_size %.1f\n\n", ap->font_family, ap->font_size);
    strbuf_append(&sb, line);
    free(line);

    for (int i = 0; i < 16; i++) {
        line = xsprintf("color%d %s\n", i, cs.ansi[i]);
        strbuf_append(&sb, line);
        free(line);
    }

    char *contents = strbuf_take(&sb);
    char *generated_path = join_path(dir, "calm-shell-theme.conf");
    bool wrote = overwrite_file(generated_path, contents);
    free(contents);

    bool wired = false;
    if (wrote) {
        char *main_conf = join_path(dir, "kitty.conf");
        char *directive = xsprintf("include %s\n", generated_path);
        wired = ensure_include_line(main_conf, "calm-shell-theme.conf", directive, "#");
        free(directive);
        free(main_conf);
    }

    if (out_detail) {
        *out_detail = wrote ? (wired ? xstrdup("kitty: theme synced (kitty.conf now includes calm-shell-theme.conf)")
                                      : xstrdup("kitty: wrote calm-shell-theme.conf, but could not update kitty.conf"))
                             : xstrdup("kitty: failed to write calm-shell-theme.conf");
    }

    color_scheme_free(&cs);
    free(generated_path);
    free(dir);
    return wrote;
}

/* ---- Alacritty ---------------------------------------------------------
 * Modern Alacritty (>=0.13) reads TOML and supports
 * `general.import = [...]`. Safely splicing a path into an *existing*
 * TOML array without a real TOML writer is fragile, so: if
 * alacritty.toml doesn't exist yet, we create it with the import;
 * if it exists and already has a `[general]` table, we leave it alone
 * and say so, rather than risk a duplicate-table TOML error. */

static bool sync_alacritty(const Theme *theme, const TermAppearance *ap, char **out_detail) {
    char *dir = config_subdir("alacritty");
    if (!dir || !mkdir_recursive(dir)) {
        free(dir);
        if (out_detail) {
            *out_detail = xstrdup("alacritty: could not create ~/.config/alacritty");
        }
        return false;
    }

    TermColorScheme cs;
    color_scheme_build(theme, &cs);

    StrBuf sb;
    strbuf_init(&sb);
    strbuf_append(&sb, "# Generated by calm-shell (`calm sync`) -- edits here are overwritten\n"
                        "# on the next sync. Change the active Calm-shell theme instead.\n\n");

    char *chunk = xsprintf("[colors.primary]\nbackground = \"%s\"\nforeground = \"%s\"\n\n"
                            "[colors.cursor]\ntext = \"%s\"\ncursor = \"%s\"\n\n",
                            cs.background, cs.foreground, cs.background, cs.cursor);
    strbuf_append(&sb, chunk);
    free(chunk);

    static const char *const names[8] = {"black", "red", "green", "yellow", "blue", "magenta", "cyan", "white"};
    strbuf_append(&sb, "[colors.normal]\n");
    for (int i = 0; i < 8; i++) {
        chunk = xsprintf("%s = \"%s\"\n", names[i], cs.ansi[i]);
        strbuf_append(&sb, chunk);
        free(chunk);
    }
    strbuf_append(&sb, "\n[colors.bright]\n");
    for (int i = 0; i < 8; i++) {
        chunk = xsprintf("%s = \"%s\"\n", names[i], cs.ansi[8 + i]);
        strbuf_append(&sb, chunk);
        free(chunk);
    }

    chunk = xsprintf("\n[font]\nsize = %.1f\n\n[font.normal]\nfamily = \"%s\"\n\n", ap->font_size, ap->font_family);
    strbuf_append(&sb, chunk);
    free(chunk);

    chunk = xsprintf("[cursor]\nstyle = { shape = \"%s\", blinking = \"%s\" }\n\n",
                      ap->cursor_style == CURSOR_STYLE_BLOCK   ? "Block"
                      : ap->cursor_style == CURSOR_STYLE_UNDERLINE ? "Underline"
                                                                    : "Beam",
                      ap->cursor_blink ? "On" : "Off");
    strbuf_append(&sb, chunk);
    free(chunk);

    chunk = xsprintf("[window]\nopacity = %.2f\npadding = { x = %lld, y = %lld }\ndecorations = \"%s\"\n",
                      ap->opacity, ap->padding, ap->padding, ap->window_decorations ? "full" : "none");
    strbuf_append(&sb, chunk);
    free(chunk);

    char *contents = strbuf_take(&sb);
    char *generated_path = join_path(dir, "calm-shell-theme.toml");
    bool wrote = overwrite_file(generated_path, contents);
    free(contents);

    bool wired = false;
    bool needs_manual = false;
    if (wrote) {
        char *main_conf = join_path(dir, "alacritty.toml");
        if (file_contains(main_conf, "calm-shell-theme.toml")) {
            wired = true;
        } else if (!path_exists(main_conf)) {
            char *directive = xsprintf("[general]\nimport = [\"%s\"]\n", generated_path);
            wired = overwrite_file(main_conf, directive);
            free(directive);
        } else if (file_contains(main_conf, "[general]")) {
            /* A `[general]` table already exists -- don't risk a
             * second one. The generated file is still up to date;
             * the user (or `calm doctor`) just needs to add our path
             * to their existing `import` list once. */
            needs_manual = true;
        } else {
            char *addition = xsprintf("\n# Added by calm-shell (`calm sync`) -- keeps this terminal's\n"
                                       "# theme matched to Calm-shell's active theme.\n[general]\nimport = [\"%s\"]\n",
                                       generated_path);
            wired = append_to_file(main_conf, addition);
            free(addition);
        }
        free(main_conf);
    }

    if (out_detail) {
        if (!wrote) {
            *out_detail = xstrdup("alacritty: failed to write calm-shell-theme.toml");
        } else if (wired) {
            *out_detail = xstrdup("alacritty: theme synced (alacritty.toml now imports calm-shell-theme.toml)");
        } else if (needs_manual) {
            *out_detail = xsprintf(
                "alacritty: wrote calm-shell-theme.toml -- add \"%s/calm-shell-theme.toml\" to your existing "
                "[general] import list to finish wiring it in",
                dir);
        } else {
            *out_detail = xstrdup("alacritty: wrote calm-shell-theme.toml, but could not update alacritty.toml");
        }
    }

    color_scheme_free(&cs);
    free(generated_path);
    free(dir);
    return wrote;
}

/* ---- WezTerm ------------------------------------------------------------
 * wezterm.lua is a real Lua program, not declarative data -- there's
 * no single safe insertion point in an arbitrary existing one. We
 * always regenerate the data-only companion module; for a *fresh*
 * install with no wezterm.lua yet, we also generate one that loads
 * it. For an existing wezterm.lua, we leave it untouched and report
 * the two lines to add by hand. */

static bool sync_wezterm(const Theme *theme, const TermAppearance *ap, char **out_detail) {
    char *dir = config_subdir("wezterm");
    if (!dir || !mkdir_recursive(dir)) {
        free(dir);
        if (out_detail) {
            *out_detail = xstrdup("wezterm: could not create ~/.config/wezterm");
        }
        return false;
    }

    TermColorScheme cs;
    color_scheme_build(theme, &cs);

    StrBuf sb;
    strbuf_init(&sb);
    strbuf_append(&sb, "-- Generated by calm-shell (`calm sync`) -- edits here are overwritten\n"
                        "-- on the next sync. Change the active Calm-shell theme instead.\nreturn {\n"
                        "  colors = {\n");
    char *chunk = xsprintf("    background = \"%s\",\n    foreground = \"%s\",\n"
                            "    cursor_bg = \"%s\",\n    cursor_border = \"%s\",\n    cursor_fg = \"%s\",\n",
                            cs.background, cs.foreground, cs.cursor, cs.cursor, cs.background);
    strbuf_append(&sb, chunk);
    free(chunk);

    strbuf_append(&sb, "    ansi = {\n");
    for (int i = 0; i < 8; i++) {
        chunk = xsprintf("      \"%s\",\n", cs.ansi[i]);
        strbuf_append(&sb, chunk);
        free(chunk);
    }
    strbuf_append(&sb, "    },\n    brights = {\n");
    for (int i = 0; i < 8; i++) {
        chunk = xsprintf("      \"%s\",\n", cs.ansi[8 + i]);
        strbuf_append(&sb, chunk);
        free(chunk);
    }
    strbuf_append(&sb, "    },\n  },\n");

    chunk = xsprintf("  font_family = \"%s\",\n  font_size = %.1f,\n"
                      "  window_background_opacity = %.2f,\n"
                      "  window_padding = { left = %lld, right = %lld, top = %lld, bottom = %lld },\n"
                      "  window_decorations = \"%s\",\n"
                      "  default_cursor_style = \"%s%s\",\n",
                      ap->font_family, ap->font_size, ap->opacity, ap->padding, ap->padding, ap->padding, ap->padding,
                      ap->window_decorations ? "TITLE | RESIZE" : "NONE",
                      ap->cursor_blink ? "Blinking" : "Steady",
                      ap->cursor_style == CURSOR_STYLE_BLOCK   ? "Block"
                      : ap->cursor_style == CURSOR_STYLE_UNDERLINE ? "Underline"
                                                                    : "Bar");
    strbuf_append(&sb, chunk);
    free(chunk);
    strbuf_append(&sb, "}\n");

    char *contents = strbuf_take(&sb);
    char *generated_path = join_path(dir, "calm-shell-theme.lua");
    bool wrote = overwrite_file(generated_path, contents);
    free(contents);

    bool wired = false;
    bool needs_manual = false;
    char *main_conf = join_path(dir, "wezterm.lua");
    if (wrote) {
        if (file_contains(main_conf, "calm-shell-theme.lua")) {
            wired = true;
        } else if (!path_exists(main_conf)) {
            const char *bootstrap = "local wezterm = require 'wezterm'\n\n"
                                     "-- Loads calm-shell's generated theme/appearance table. Regenerated by\n"
                                     "-- `calm sync`; add your own settings below the merge if you like.\n"
                                     "local config = dofile(wezterm.config_dir .. '/calm-shell-theme.lua')\n\n"
                                     "return config\n";
            wired = overwrite_file(main_conf, bootstrap);
        } else {
            needs_manual = true;
        }
    }

    if (out_detail) {
        if (!wrote) {
            *out_detail = xstrdup("wezterm: failed to write calm-shell-theme.lua");
        } else if (wired) {
            *out_detail = xstrdup("wezterm: theme synced (wezterm.lua loads calm-shell-theme.lua)");
        } else if (needs_manual) {
            *out_detail =
                xstrdup("wezterm: wrote calm-shell-theme.lua -- merge `dofile(wezterm.config_dir .. "
                         "'/calm-shell-theme.lua')` into your existing wezterm.lua to finish wiring it in");
        } else {
            *out_detail = xstrdup("wezterm: wrote calm-shell-theme.lua, but could not create wezterm.lua");
        }
    }

    color_scheme_free(&cs);
    free(generated_path);
    free(main_conf);
    free(dir);
    return wrote;
}

/* ---- Foot --------------------------------------------------------------
 * foot.ini supports a top-level `include=<path>` directive (foot
 * >=1.14), so wiring works the same way as Kitty's `include`. Foot's
 * own hex colors are written without a leading '#'. */

static bool sync_foot(const Theme *theme, const TermAppearance *ap, char **out_detail) {
    char *dir = config_subdir("foot");
    if (!dir || !mkdir_recursive(dir)) {
        free(dir);
        if (out_detail) {
            *out_detail = xstrdup("foot: could not create ~/.config/foot");
        }
        return false;
    }

    TermColorScheme cs;
    color_scheme_build(theme, &cs);

    StrBuf sb;
    strbuf_init(&sb);
    strbuf_append(&sb, "# Generated by calm-shell (`calm sync`) -- edits here are overwritten\n"
                        "# on the next sync. Change the active Calm-shell theme instead.\n\n[main]\n");
    char *chunk = xsprintf("font=%s:size=%.0f\npad=%lldx%lld\n\n", ap->font_family, ap->font_size, ap->padding,
                            ap->padding);
    strbuf_append(&sb, chunk);
    free(chunk);

    chunk = xsprintf("[cursor]\nstyle=%s\nblink=%s\ncolor=%s %s\n\n", cursor_style_kitty(ap->cursor_style),
                      ap->cursor_blink ? "yes" : "no", strip_hash(cs.background), strip_hash(cs.cursor));
    strbuf_append(&sb, chunk);
    free(chunk);

    strbuf_append(&sb, "[colors]\n");
    chunk = xsprintf("background=%s\nforeground=%s\nalpha=%.2f\n\n", strip_hash(cs.background),
                      strip_hash(cs.foreground), ap->opacity);
    strbuf_append(&sb, chunk);
    free(chunk);
    for (int i = 0; i < 8; i++) {
        chunk = xsprintf("regular%d=%s\n", i, strip_hash(cs.ansi[i]));
        strbuf_append(&sb, chunk);
        free(chunk);
    }
    for (int i = 0; i < 8; i++) {
        chunk = xsprintf("bright%d=%s\n", i, strip_hash(cs.ansi[8 + i]));
        strbuf_append(&sb, chunk);
        free(chunk);
    }

    if (!ap->window_decorations) {
        /* Foot only draws its own decorations in CSD mode; zeroing
         * the border is the closest equivalent to "no decorations"
         * it exposes. Left alone (server-side/compositor default)
         * when decorations are wanted. */
        strbuf_append(&sb, "\n[csd]\nsize=0\nborder-width=0\n");
    }

    char *contents = strbuf_take(&sb);
    char *generated_path = join_path(dir, "calm-shell-theme.ini");
    bool wrote = overwrite_file(generated_path, contents);
    free(contents);

    bool wired = false;
    if (wrote) {
        char *main_conf = join_path(dir, "foot.ini");
        char *directive = xsprintf("include=%s\n", generated_path);
        wired = ensure_include_line(main_conf, "calm-shell-theme.ini", directive, "#");
        free(directive);
        free(main_conf);
    }

    if (out_detail) {
        *out_detail = wrote ? (wired ? xstrdup("foot: theme synced (foot.ini now includes calm-shell-theme.ini)")
                                      : xstrdup("foot: wrote calm-shell-theme.ini, but could not update foot.ini"))
                             : xstrdup("foot: failed to write calm-shell-theme.ini");
    }

    color_scheme_free(&cs);
    free(generated_path);
    free(dir);
    return wrote;
}

