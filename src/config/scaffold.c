static bool write_file_if_missing(const char *path, const char *contents) {
    if (path_exists(path)) {
        return true; /* already exists, leave user edits alone */
    }
    FILE *f = fopen(path, "w");
    if (!f) {
        return false;
    }
    size_t len = strlen(contents);
    bool ok = fwrite(contents, 1, len, f) == len;
    fclose(f);
    return ok;
}

