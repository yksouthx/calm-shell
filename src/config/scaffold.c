/* Backs both ensure_scaffold() and scaffold_defaults() -- a single
 * source of truth for "every topic file scaffold.c owns, and what its
 * default content is", so `calm repair` restoring one corrupt file
 * can't drift out of sync with what a fresh install actually gets.
 *
 * Order matters here beyond readability: config.calm is deliberately
 * listed LAST. `calm_parse_file` follows `include` directives, so
 * parsing config.calm fails if *any* included topic file is broken --
 * not just if config.calm's own content is. `calm repair` walks this
 * table in order, so every included file gets validated/restored
 * first; by the time config.calm itself is checked, a failure there
 * really is config.calm's own fault, not a corrupt include bubbling
 * up and causing config.calm to be needlessly backed up and reset to
 * defaults over a typo in a completely different file. */
static const ScaffoldDefault SCAFFOLD_DEFAULTS[11] = {
    {environment_file, default_environment_calm, "environment.calm"},
    {directory_file, default_directory_calm, "directory.calm"},
    {history_settings_file, default_history_calm, "history.calm"},
    {keyboard_file, default_keyboard_calm, "keyboard.calm"},
    {terminal_file, default_terminal_calm, "terminal.calm"},
    {aliases_file, default_aliases_calm, "aliases.calm"},
    {functions_file, default_functions_calm, "functions.calm"},
    {plugins_state_file, default_plugins_calm, "plugins.calm"},
    {plugins_active_file, default_plugins_active_calm, "plugins_active.calm"},
    {profile_active_file, default_profile_active_calm, "profile_active.calm"},
    {config_file, default_config_calm, "config.calm"},
};

const ScaffoldDefault *scaffold_defaults(size_t *out_count) {
    if (out_count) {
        *out_count = sizeof(SCAFFOLD_DEFAULTS) / sizeof(SCAFFOLD_DEFAULTS[0]);
    }
    return SCAFFOLD_DEFAULTS;
}

/* One-time migration for installs scaffolded before the profile/plugin
 * systems existed: their config.calm has no `include
 * "plugins_active.calm"`/`include "profile_active.calm"` line, so a
 * plugin's or profile's overlay would silently never be read. Uses
 * the same idempotent append pattern fsutil's file_contains() is
 * meant for -- safe to call every run, a no-op once the line is
 * present.
 *
 * Known limitation: this always appends to the *end* of the file, so
 * an install that already migrated in profile_active.calm (from an
 * older calm-shell that had profiles but not yet plugins) will end up
 * with plugins_active.calm's include appearing *after*
 * profile_active.calm's -- inverting default_config_calm()'s intended
 * "profile wins over plugin" precedence for that one upgrade path. A
 * fresh scaffold (or any install migrating both at once, this
 * version's actual release) gets the right order. Reordering existing
 * include lines in place isn't worth the risk of mangling a config
 * file the user may have since hand-edited around either line. */
static bool migrate_generated_include(const char *cfg_path, const char *basename) {
    if (!path_is_file(cfg_path)) {
        return true; /* nothing to migrate -- default_config_calm() already has it */
    }
    if (file_contains(cfg_path, basename)) {
        return true;
    }
    char *addition = xsprintf("\n"
                               "# Added by a calm-shell upgrade: see default_config_calm() in\n"
                               "# config/scaffold.c for why this must stay last.\n"
                               "include \"%s\"\n",
                               basename);
    bool ok = append_to_file(cfg_path, addition);
    free(addition);
    return ok;
}

bool ensure_scaffold(void) {
    char *dirs[5] = {config_dir(), themes_dir(), plugins_dir(), history_dir(), profiles_dir()};
    bool ok = true;
    for (int i = 0; i < 5; i++) {
        if (!dirs[i] || !mkdir_recursive(dirs[i])) {
            ok = false;
        }
    }
    if (ok) {
        size_t count = 0;
        const ScaffoldDefault *defaults = scaffold_defaults(&count);
        for (size_t i = 0; i < count && ok; i++) {
            char *path = defaults[i].path_fn();
            if (!path || !write_file_if_missing(path, defaults[i].content_fn())) {
                ok = false;
            }
            free(path);
        }

        char *themes = themes_dir();
        if (themes) {
            char *lavender_path = join_path(themes, "calm-lavender.calm");
            write_file_if_missing(lavender_path, default_lavender_theme());
            free(lavender_path);
            free(themes);
        }

        char *cfg_path = config_file();
        if (cfg_path) {
            ok = migrate_generated_include(cfg_path, "plugins_active.calm") && ok;
            ok = migrate_generated_include(cfg_path, "profile_active.calm") && ok;
            free(cfg_path);
        }
    }
    for (int i = 0; i < 5; i++) {
        free(dirs[i]);
    }
    return ok;
}
